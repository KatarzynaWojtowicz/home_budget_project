package pl.homebudget.app.homebudget.to;

public class ProfitTo {
	private String name;
	private double amount;
	private String dateProfit;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public double getAmount() {
		return amount;
	}

	public void setAmount(double amount) {
		this.amount = amount;
	}

	public String getDateProfit() {
		return dateProfit;
	}

	public void setDateProfit(String dateProfit) {
		this.dateProfit = dateProfit;
	}

}
